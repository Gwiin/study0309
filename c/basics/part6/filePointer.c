#include <stdio.h>

int main(void){

    FILE *a;
    a = fopen("test2.dat","w"); //파일을 쓰기 모드로 엽니다. 파일이 없으면 새로 생성되고, 있으면 기존 내용을 덮어씁니다.
    if(a==NULL)
    {
        printf("파일을 열수 없다.\n");
        return 1; // 파일 열기에 실패했을 때 프로그램을 종료합니다.
    }
    // fprintf(a, "이것은 파일포인터로 저장되는 예시 데이터 입니다.");
    fputs("이것은 파일포인터로 저장되는 예시 데이터 입니다.", a); 
    
    fclose(a);
    return 0;

}

//filedescriptor.c 파일에서는 파일 디스크립터를 사용하여 파일에 데이터를 쓰는 예시를 보여주고 있습니다. 반면, filePointer.c 파일에서는 파일 포인터를 사용하여 파일에 데이터를 쓰는 예시를 보여주고 있습니다. 파일 포인터는 C 표준 라이브러리에서 제공하는 FILE 구조체를 사용하여 파일을 다루는 방식입니다. fopen() 함수를 사용하여 파일을 열고, fprintf() 함수를 사용하여 데이터를 쓰며, fclose() 함수를 사용하여 파일을 닫습니다. 이 방법은 버퍼링이 자동으로 처리되므로, 데이터를 메모리에 임시로 저장한 후 한 번에 파일에 기록합니다. 따라서 대량의 데이터를 처리하거나 성능이 중요한 경우에는 버퍼링을 사용하는 것이 좋습니다.

//fprintf() 함수와 fputs() 함수의 차이는 fprintf() 함수는 서식 지정이 가능한 출력 함수로, 다양한 데이터 형식을 출력할 수 있습니다. 반면, fputs() 함수는 문자열을 파일에 쓰는 간단한 함수로, 서식 지정이 필요 없는 경우에 사용됩니다. 이 예시에서는 fputs() 함수를 사용하여 문자열을 파일에 쓰고 있습니다.